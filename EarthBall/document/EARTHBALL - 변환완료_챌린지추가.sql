CREATE TABLE TB_CHALLENGE
(
    CH_NO        NUMBER NOT NULL,
    CH_TITLE     VARCHAR2(100) NOT NULL,
    CH_START_DAY VARCHAR2(8) NOT NULL,
    CH_END_DAY   VARCHAR2(8) NOT NULL,
    MEMBER_ID    VARCHAR2(100) NOT NULL,
