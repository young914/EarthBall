CREATE TABLE STORE_REGION
(
    STORE_REGION_NO NUMBER PRIMARY KEY,
    CITY            VARCHAR2(50),
    PROVINCES       VARCHAR2(50),
    CONSTRAINT city_province_unique UNIQUE (CITY, PROVINCES)
);

CREATE TABLE STORE
(
    STORE_NO        NUMBER           NOT NULL,
    STORE_NAME      VARCHAR2(50) NOT NULL,
    STORE_ADDRESS   VARCHAR2(200) NOT NULL,
    STORE_PHONE     VARCHAR2(30) NULL,
    BUSINESS_HOURS  VARCHAR2(20) NULL,
    STORE_RC        NUMBER DEFAULT 0 NOT NULL,
    STORE_LAT       NUMBER NULL,
    STORE_LON       NUMBER NULL,
    STORE_REGION_NO NUMBER NULL,
    STORE_INFO      VARCHAR2(500),
